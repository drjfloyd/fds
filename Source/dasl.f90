! --------------------------- DGEFA -------------------------------------------

SUBROUTINE DGEFA(A,LDA,N,IPVT,INFO)

IMPLICIT NONE
INTEGER :: LDA, N, IPVT(*), INFO
REAL(EB) :: A(LDA,*)
!
!     DGEFA FACTORS A REAL(EB) MATRIX BY GAUSSIAN ELIMINATION.
!
!     DGEFA IS USUALLY CALLED BY DGECO, BUT IT CAN BE CALLED
!     DIRECTLY WITH A SAVING IN TIME IF  RCOND  IS NOT NEEDED.
!     (TIME FOR DGECO) = (1 + 9/N)*(TIME FOR DGEFA) .
!
!     ON ENTRY
!
!        A       REAL(EB)(LDA, N)
!                THE MATRIX TO BE FACTORED.
!
!        LDA     INTEGER
!                THE LEADING DIMENSION OF THE ARRAY  A .
!
!        N       INTEGER
!                THE ORDER OF THE MATRIX  A .
!
!     ON RETURN
!
!        A       AN UPPER TRIANGULAR MATRIX AND THE MULTIPLIERS
!                WHICH WERE USED TO OBTAIN IT.
!                THE FACTORIZATION CAN BE WRITTEN  A = L*U  WHERE
!                L  IS A PRODUCT OF PERMUTATION AND UNIT LOWER
!                TRIANGULAR MATRICES AND  U  IS UPPER TRIANGULAR.
!
!        IPVT    INTEGER(N)
!                AN INTEGER VECTOR OF PIVOT INDICES.
!
!        INFO    INTEGER
!                = 0  NORMAL VALUE.
!                = K  IF  U(K,K) == 0.0 .  THIS IS NOT AN ERROR
!                     CONDITION FOR THIS SUBROUTINE, BUT IT DOES
!                     INDICATE THAT DGESL OR DGEDI WILL DIVIDE BY ZERO
!                     IF CALLED.  USE  RCOND  IN DGECO FOR A RELIABLE
!                     INDICATION OF SINGULARITY.
!
!     LINPACK. THIS VERSION DATED 08/14/78 .
!     CLEVE MOLER, UNIVERSITY OF NEW MEXICO, ARGONNE NATIONAL LAB.
!
!     SUBROUTINES AND FUNCTIONS
!
!     BLAS DAXPY,DSCAL,IDAMAX
!
!     INTERNAL VARIABLES
!
REAL(EB) :: T
INTEGER :: J, K, KP1, L, NM1
!
!
!     GAUSSIAN ELIMINATION WITH PARTIAL PIVOTING
!
INFO = 0
NM1 = N - 1
IF (NM1>=1) THEN
DO K = 1, NM1
KP1 = K + 1
!
!        FIND L = PIVOT INDEX
!
L = IDAMAX(N-K+1,A(K,K),1) + K - 1
IPVT(K) = L
!
!        ZERO PIVOT IMPLIES THIS COLUMN ALREADY TRIANGULARIZED
!
IF (A(L,K)/=0.0_EB) THEN
      !
      !           INTERCHANGE IF NECESSARY
      !
      IF (L/=K) THEN
         T = A(L,K)
         A(L,K) = A(K,K)
         A(K,K) = T
      END IF
      !
      !           COMPUTE MULTIPLIERS
      !
      T = -1.0_EB / A(K,K)
      CALL DSCAL(N-K,T,A(K+1,K),1)
      !
      !           ROW ELIMINATION WITH COLUMN INDEXING
      !
      DO J = KP1, N
         T = A(L,J)
         IF (L/=K) THEN
            A(L,J) = A(K,J)
            A(K,J) = T
         END IF
         CALL DAXPY(N-K,T,A(K+1,K),1,A(K+1,J),1)
      END DO
ELSE
      INFO = K
END IF
END DO
END IF
IPVT(N) = N
IF (A(N,N)==0.0_EB) INFO = N
RETURN
END SUBROUTINE DGEFA

! --------------------------- DGESL -------------------------------------------

SUBROUTINE DGESL(A,LDA,N,IPVT,B,JOB)

IMPLICIT NONE
INTEGER :: LDA, N, IPVT(*), JOB
REAL(EB) :: A(LDA,*), B(*)
!
!     DGESL SOLVES THE REAL(EB) SYSTEM
!     A * X = B  OR  TRANS(A) * X = B
!     USING THE FACTORS COMPUTED BY DGECO OR DGEFA.
!
!     ON ENTRY
!
!        A       REAL(EB)(LDA, N)
!                THE OUTPUT FROM DGECO OR DGEFA.
!
!        LDA     INTEGER
!                THE LEADING DIMENSION OF THE ARRAY  A .
!
!        N       INTEGER
!                THE ORDER OF THE MATRIX  A .
!
!        IPVT    INTEGER(N)
!                THE PIVOT VECTOR FROM DGECO OR DGEFA.
!
!        B       REAL(EB)(N)
!                THE RIGHT HAND SIDE VECTOR.
!
!        JOB     INTEGER
!                = 0         TO SOLVE  A*X = B ,
!                = NONZERO   TO SOLVE  TRANS(A)*X = B  WHERE
!                            TRANS(A)  IS THE TRANSPOSE.
!
!     ON RETURN
!
!        B       THE SOLUTION VECTOR  X .
!
!     ERROR CONDITION
!
!        A DIVISION BY ZERO WILL OCCUR IF THE INPUT FACTOR CONTAINS A
!        ZERO ON THE DIAGONAL.  TECHNICALLY THIS INDICATES SINGULARITY
!        BUT IT IS OFTEN CAUSED BY IMPROPER ARGUMENTS OR IMPROPER
!        SETTING OF LDA .  IT WILL NOT OCCUR IF THE SUBROUTINES ARE
!        CALLED CORRECTLY AND IF DGECO HAS SET RCOND > 0.0
!        OR DGEFA HAS SET INFO == 0 .
!
!     TO COMPUTE  INVERSE(A) * C  WHERE  C  IS A MATRIX
!     WITH  P  COLUMNS
!           CALL DGECO(A,LDA,N,IPVT,RCOND,Z)
!           IF (RCOND IS TOO SMALL) GO TO ...
!           DO 10 J = 1, P
!              CALL DGESL(A,LDA,N,IPVT,C(1,J),0)
!        10 CONTINUE
!
!     LINPACK. THIS VERSION DATED 08/14/78 .
!     CLEVE MOLER, UNIVERSITY OF NEW MEXICO, ARGONNE NATIONAL LAB.
!
!     SUBROUTINES AND FUNCTIONS
!
!     BLAS DAXPY,DDOT
!
!     INTERNAL VARIABLES
!
REAL(EB) :: T
INTEGER :: K, KB, L, NM1
!
NM1 = N - 1
IF (JOB==0) THEN
!
!        JOB = 0 , SOLVE  A * X = B
!        FIRST SOLVE  L*Y = B
!
IF (NM1>=1) THEN
DO K = 1, NM1
      L = IPVT(K)
      T = B(L)
      IF (L/=K) THEN
         B(L) = B(K)
         B(K) = T
      END IF
      CALL DAXPY(N-K,T,A(K+1,K),1,B(K+1),1)
END DO
END IF
!
!        NOW SOLVE  U*X = Y
!
DO KB = 1, N
K = N + 1 - KB
B(K) = B(K) / A(K,K)
T = -B(K)
CALL DAXPY(K-1,T,A(1,K),1,B(1),1)
END DO
ELSE
!
!        JOB = NONZERO, SOLVE  TRANS(A) * X = B
!        FIRST SOLVE  TRANS(U)*Y = B
!
DO K = 1, N
T = DDOT(K-1,A(1,K),1,B(1),1)
B(K) = (B(K)-T) / A(K,K)
END DO
!
!        NOW SOLVE TRANS(L)*X = Y
!
IF (NM1>=1) THEN
DO KB = 1, NM1
      K = N - KB
      B(K) = B(K) + DDOT(N-K,A(K+1,K),1,B(K+1),1)
      L = IPVT(K)
      IF (L/=K) THEN
         T = B(L)
         B(L) = B(K)
         B(K) = T
      END IF
END DO
END IF
END IF
RETURN
END SUBROUTINE DGESL

! --------------------------- DGBFA -------------------------------------------

SUBROUTINE DGBFA(ABD,LDA,N,ML,MU,IPVT,INFO)
IMPLICIT NONE
INTEGER :: LDA, N, ML, MU, IPVT(*), INFO
REAL(EB) :: ABD(LDA,*)
!
!     DGBFA FACTORS A REAL(EB) BAND MATRIX BY ELIMINATION.
!
!     DGBFA IS USUALLY CALLED BY DGBCO, BUT IT CAN BE CALLED
!     DIRECTLY WITH A SAVING IN TIME IF  RCOND  IS NOT NEEDED.
!
!     ON ENTRY
!
!        ABD     REAL(EB)(LDA, N)
!                CONTAINS THE MATRIX IN BAND STORAGE.  THE COLUMNS
!                OF THE MATRIX ARE STORED IN THE COLUMNS OF  ABD  AND
!                THE DIAGONALS OF THE MATRIX ARE STORED IN ROWS
!                ML+1 THROUGH 2*ML+MU+1 OF  ABD .
!                SEE THE COMMENTS BELOW FOR DETAILS.
!
!        LDA     INTEGER
!                THE LEADING DIMENSION OF THE ARRAY  ABD .
!                LDA MUST BE >= 2*ML + MU + 1 .
!
!        N       INTEGER
!                THE ORDER OF THE ORIGINAL MATRIX.
!
!        ML      INTEGER
!                NUMBER OF DIAGONALS BELOW THE MAIN DIAGONAL.
!                0 <= ML < N .
!
!        MU      INTEGER
!                NUMBER OF DIAGONALS ABOVE THE MAIN DIAGONAL.
!                0 <= MU < N .
!                MORE EFFICIENT IF  ML <= MU .
!     ON RETURN
!
!        ABD     AN UPPER TRIANGULAR MATRIX IN BAND STORAGE AND
!                THE MULTIPLIERS WHICH WERE USED TO OBTAIN IT.
!                THE FACTORIZATION CAN BE WRITTEN  A = L*U  WHERE
!                L  IS A PRODUCT OF PERMUTATION AND UNIT LOWER
!                TRIANGULAR MATRICES AND  U  IS UPPER TRIANGULAR.
!
!        IPVT    INTEGER(N)
!                AN INTEGER VECTOR OF PIVOT INDICES.
!
!        INFO    INTEGER
!                = 0  NORMAL VALUE.
!                = K  IF  U(K,K) == 0.0 .  THIS IS NOT AN ERROR
!                     CONDITION FOR THIS SUBROUTINE, BUT IT DOES
!                     INDICATE THAT DGBSL WILL DIVIDE BY ZERO IF
!                     CALLED.  USE  RCOND  IN DGBCO FOR A RELIABLE
!                     INDICATION OF SINGULARITY.
!
!     BAND STORAGE
!
!           IF  A  IS A BAND MATRIX, THE FOLLOWING PROGRAM SEGMENT
!           WILL SET UP THE INPUT.
!
!                   ML = (BAND WIDTH BELOW THE DIAGONAL)
!                   MU = (BAND WIDTH ABOVE THE DIAGONAL)
!                   M = ML + MU + 1
!                   DO 20 J = 1, N
!                      I1 = MAX0(1, J-MU)
!                      I2 = MIN0(N, J+ML)
!                      DO 10 I = I1, I2
!                         K = I - J + M
!                         ABD(K,J) = A(I,J)
!                10    CONTINUE
!                20 CONTINUE
!
!           THIS USES ROWS  ML+1  THROUGH  2*ML+MU+1  OF  ABD .
!           IN ADDITION, THE FIRST  ML  ROWS IN  ABD  ARE USED FOR
!           ELEMENTS GENERATED DURING THE TRIANGULARIZATION.
!           THE TOTAL NUMBER OF ROWS NEEDED IN  ABD  IS  2*ML+MU+1 .
!           THE  ML+MU BY ML+MU  UPPER LEFT TRIANGLE AND THE
!           ML BY ML  LOWER RIGHT TRIANGLE ARE NOT REFERENCED.
!
!     LINPACK. THIS VERSION DATED 08/14/78 .
!     CLEVE MOLER, UNIVERSITY OF NEW MEXICO, ARGONNE NATIONAL LAB.
!
!     SUBROUTINES AND FUNCTIONS
!
!     BLAS DAXPY,DSCAL,IDAMAX
!     FORTRAN MAX0,MIN0
!
!     INTERNAL VARIABLES
!
REAL(EB) :: T
INTEGER :: I, I0, J, JU, JZ, J0, J1, K, KP1, L, LM, M, MM, NM1
!
!
M = ML + MU + 1
INFO = 0
!
!     ZERO INITIAL FILL-IN COLUMNS
!
J0 = MU + 2
J1 = MIN(N,M) - 1
IF (J1>=J0) THEN
DO JZ = J0, J1
I0 = M + 1 - JZ
DO I = I0, ML
      ABD(I,JZ) = 0.0_EB
END DO
END DO
END IF
JZ = J1
JU = 0
!
!     GAUSSIAN ELIMINATION WITH PARTIAL PIVOTING
!
NM1 = N - 1
IF (NM1>=1) THEN
DO K = 1, NM1
KP1 = K + 1
!
!        ZERO N_MVEXT FILL-IN COLUMN
!
JZ = JZ + 1
IF (JZ<=N) THEN
      IF (ML>=1) THEN
         DO I = 1, ML
            ABD(I,JZ) = 0.0_EB
         END DO
      END IF
END IF
!
!        FIND L = PIVOT INDEX
!
LM = MIN(ML,N-K)
L = IDAMAX(LM+1,ABD(M,K),1) + M - 1
IPVT(K) = L + K - M
!
!        ZERO PIVOT IMPLIES THIS COLUMN ALREADY TRIANGULARIZED
!
IF (ABD(L,K)/=0.0_EB) THEN
      !
      !           INTERCHANGE IF NECESSARY
      !
      IF (L/=M) THEN
         T = ABD(L,K)
         ABD(L,K) = ABD(M,K)
         ABD(M,K) = T
      END IF
      !
      !           COMPUTE MULTIPLIERS
      !
      T = -1.0_EB / ABD(M,K)
      CALL DSCAL(LM,T,ABD(M+1,K),1)
      !
      !           ROW ELIMINATION WITH COLUMN INDEXING
      !
      JU = MIN(MAX(JU,MU+IPVT(K)),N)
      MM = M
      IF (JU>=KP1) THEN
         DO J = KP1, JU
            L = L - 1
            MM = MM - 1
            T = ABD(L,J)
            IF (L/=MM) THEN
                  ABD(L,J) = ABD(MM,J)
                  ABD(MM,J) = T
            END IF
            CALL DAXPY(LM,T,ABD(M+1,K),1,ABD(MM+1,J),1)
         END DO
      END IF
ELSE
      INFO = K
END IF
END DO
END IF
IPVT(N) = N
IF (ABD(M,N)==0.0_EB) INFO = N
RETURN
END SUBROUTINE DGBFA

! --------------------------- DGBSL -------------------------------------------

SUBROUTINE DGBSL(ABD,LDA,N,ML,MU,IPVT,B,JOB)

IMPLICIT NONE
INTEGER :: LDA, N, ML, MU, IPVT(*), JOB
REAL(EB) :: ABD(LDA,*), B(*)
!
!     DGBSL SOLVES THE REAL(EB) BAND SYSTEM
!     A * X = B  OR  TRANS(A) * X = B
!     USING THE FACTORS COMPUTED BY DGBCO OR DGBFA.
!
!     ON ENTRY
!
!        ABD     REAL(EB)(LDA, N)
!                THE OUTPUT FROM DGBCO OR DGBFA.
!
!        LDA     INTEGER
!                THE LEADING DIMENSION OF THE ARRAY  ABD .
!
!        N       INTEGER
!                THE ORDER OF THE ORIGINAL MATRIX.
!
!        ML      INTEGER
!                NUMBER OF DIAGONALS BELOW THE MAIN DIAGONAL.
!
!        MU      INTEGER
!                NUMBER OF DIAGONALS ABOVE THE MAIN DIAGONAL.
!
!        IPVT    INTEGER(N)
!                THE PIVOT VECTOR FROM DGBCO OR DGBFA.
!
!        B       REAL(EB)(N)
!                THE RIGHT HAND SIDE VECTOR.
!
!        JOB     INTEGER
!                = 0         TO SOLVE  A*X = B ,
!                = NONZERO   TO SOLVE  TRANS(A)*X = B , WHERE
!                            TRANS(A)  IS THE TRANSPOSE.
!
!     ON RETURN
!
!        B       THE SOLUTION VECTOR  X .
!
!     ERROR CONDITION
!
!        A DIVISION BY ZERO WILL OCCUR IF THE INPUT FACTOR CONTAINS A
!        ZERO ON THE DIAGONAL.  TECHNICALLY THIS INDICATES SINGULARITY
!        BUT IT IS OFTEN CAUSED BY IMPROPER ARGUMENTS OR IMPROPER
!        SETTING OF LDA .  IT WILL NOT OCCUR IF THE SUBROUTINES ARE
!        CALLED CORRECTLY AND IF DGBCO HAS SET RCOND > 0.0
!        OR DGBFA HAS SET INFO == 0 .
!
!     TO COMPUTE  INVERSE(A) * C  WHERE  C  IS A MATRIX
!     WITH  P  COLUMNS
!           CALL DGBCO(ABD,LDA,N,ML,MU,IPVT,RCOND,Z)
!           IF (RCOND IS TOO SMALL) GO TO ...
!           DO 10 J = 1, P
!              CALL DGBSL(ABD,LDA,N,ML,MU,IPVT,C(1,J),0)
!        10 CONTINUE
!
!     LINPACK. THIS VERSION DATED 08/14/78 .
!     CLEVE MOLER, UNIVERSITY OF NEW MEXICO, ARGONNE NATIONAL LAB.
!
!     SUBROUTINES AND FUNCTIONS
!
!     BLAS DAXPY,DDOT
!     FORTRAN MIN0
!
!     INTERNAL VARIABLES
!
REAL(EB) :: T
INTEGER :: K, KB, L, LA, LB, LM, M, NM1
!
M = MU + ML + 1
NM1 = N - 1
IF (JOB==0) THEN
!
!        JOB = 0 , SOLVE  A * X = B
!        FIRST SOLVE L*Y = B
!
IF (ML/=0) THEN
IF (NM1>=1) THEN
      DO K = 1, NM1
         LM = MIN(ML,N-K)
         L = IPVT(K)
         T = B(L)
         IF (L/=K) THEN
            B(L) = B(K)
            B(K) = T
         END IF
         CALL DAXPY(LM,T,ABD(M+1,K),1,B(K+1),1)
      END DO
END IF
END IF
!
!        NOW SOLVE  U*X = Y
!
DO KB = 1, N
K = N + 1 - KB
B(K) = B(K) / ABD(M,K)
LM = MIN(K,M) - 1
LA = M - LM
LB = K - LM
T = -B(K)
CALL DAXPY(LM,T,ABD(LA,K),1,B(LB),1)
END DO
ELSE
!
!        JOB = NONZERO, SOLVE  TRANS(A) * X = B
!        FIRST SOLVE  TRANS(U)*Y = B
!
DO K = 1, N
LM = MIN(K,M) - 1
LA = M - LM
LB = K - LM
T = DDOT(LM,ABD(LA,K),1,B(LB),1)
B(K) = (B(K)-T) / ABD(M,K)
END DO
!
!        NOW SOLVE TRANS(L)*X = Y
!
IF (ML/=0) THEN
IF (NM1>=1) THEN
      DO KB = 1, NM1
         K = N - KB
         LM = MIN(ML,N-K)
         B(K) = B(K) + DDOT(LM,ABD(M+1,K),1,B(K+1),1)
         L = IPVT(K)
         IF (L/=K) THEN
            T = B(L)
            B(L) = B(K)
            B(K) = T
         END IF
      END DO
END IF
END IF
END IF
RETURN
END SUBROUTINE DGBSL

