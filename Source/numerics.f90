REAL(8) FUNCTION DDOT(N,DX,INCX,DY,INCY)

!***BEGIN PROLOGUE  DDOT
!***DATE WRITTEN   791001   (YYMMDD)
!***REVISION DATE  820801   (YYMMDD)
!***CATEGORY NO.  D1A4
!***KEYWORDS  BLAS,REAL(8),INNER PRODUCT,LINEAR ALGEBRA,VECTOR
!***AUTHOR  LAWSON, C. L., (JPL)
!           HANSON, R. J., (SNLA)
