!***END PROLOGUE  IDAMAX
!
IMPLICIT NONE
REAL(8) :: DX(*),DMAX,XMAG
INTEGER :: N, INCX, NS, II, I
!***FIRST EXECUTABLE STATEMENT  IDAMAX
IDAMAX = 0
IF (N<=0) RETURN
IDAMAX = 1
IF (N<=1)RETURN
IF (INCX /= 1) THEN
   !
   !        CODE FOR INCREMENTS NOT EQUAL TO 1.
   !
   DMAX = ABS(DX(1))
   NS = N*INCX
   II = 1
   DO I = 1,NS,INCX
      XMAG = ABS(DX(I))
      IF (XMAG>DMAX) THEN
         IDAMAX = II
         DMAX = XMAG
      ENDIF
      II = II + 1
   END DO
   RETURN
ENDIF
!
!        CODE FOR INCREMENTS EQUAL TO 1.
!
DMAX = ABS(DX(1))
DO I = 2,N
   XMAG = ABS(DX(I))
   IF (XMAG>DMAX) THEN
      IDAMAX = I
      DMAX = XMAG
   ENDIF
END DO

RETURN

END FUNCTION IDAMAX

