!           KROGH, F. T., (JPL)
!***PURPOSE  D.P. INNER PRODUCT OF D.P. VECTORS
!***DESCRIPTION
!
!                B L A S  SUBPROGRAM
!    DESCRIPTION OF PARAMETERS
!
!     --INPUT--
!        N  NUMBER OF ELEMENTS IN INPUT VECTOR(S)
!       DX  REAL(8) VECTOR WITH N ELEMENTS
!     INCX  STORAGE SPACING BETWEEN ELEMENTS OF DX
!       DY  REAL(8) VECTOR WITH N ELEMENTS
!     INCY  STORAGE SPACING BETWEEN ELEMENTS OF DY
!
!     --OUTPUT--
!     DDOT  REAL(8) DOT PRODUCT (ZERO IF N <= 0)
!
!     RETURNS THE DOT PRODUCT OF REAL(8) DX AND DY.
!     DDOT = SUM FOR I = 0 TO N-1 OF  DX(LX+I*INCX) * DY(LY+I*INCY)
!     WHERE LX = 1 IF INCX >= 0, ELSE LX = (-INCX)*N, AND LY IS
!     DEFINED IN A SIMILAR WAY USING INCY.
!***REFERENCES  LAWSON C.L., HANSON R.J., KINCAID D.R., KROGH F.T.,
!                 *BASIC LINEAR ALGEBRA SUBPROGRAMS FOR FORTRAN USAGE*,
!                 ALGORITHM NO. 539, TRANSACTIONS ON MATHEMATICAL
!                 SOFTWARE, VOLUME 5, NUMBER 3, SEPTEMBER 1979, 308-323
!***ROUTINES CALLED  (NONE)
!***END PROLOGUE  DDOT
!

IMPLICIT NONE
REAL(8) :: DX(*),DY(*)
INTEGER :: N, INCX, INCY, IX, IY, I, M, MP1, NS

!***FIRST EXECUTABLE STATEMENT  DDOT
DDOT = 0.D0
IF (N<=0)RETURN
IF (INCX==INCY) THEN
   ! IF (INCX-1) 5,20,60
   IF (INCX-1.LT.0) THEN
      !
      !         CODE FOR UNEQUAL OR NONPOSITIVE INCREMENTS.
      !
      IX = 1
      IY = 1
      IF (INCX<0)IX = (-N+1)*INCX + 1
      IF (INCY<0)IY = (-N+1)*INCY + 1
      DO I = 1,N
         DDOT = DDOT + DX(IX)*DY(IY)
         IX = IX + INCX
         IY = IY + INCY
      END DO
   ELSE IF (INCX-1.EQ.0) THEN
      !
      !        CODE FOR BOTH INCREMENTS EQUAL TO 1.
      !
      !
      !        CLEAN-UP LOOP SO REMAINING VECTOR LENGTH IS A MULTIPLE OF 5.
      !
      M = MODULO(N,5)
      IF ( M /= 0 ) THEN
         DO I = 1,M
            DDOT = DDOT + DX(I)*DY(I)
         END DO
         IF ( N < 5 ) RETURN
      ENDIF
      MP1 = M + 1
      DO I = MP1,N,5
         DDOT = DDOT + DX(I)*DY(I) + DX(I+1)*DY(I+1) + DX(I + 2)*DY(I + 2) + DX(I + 3)*DY(I + 3) + DX(I + 4)*DY(I + 4)
      END DO
   ELSE
      !
      !         CODE FOR POSITIVE EQUAL INCREMENTS /=1.
      !
      NS = N*INCX
      DO I=1,NS,INCX
         DDOT = DDOT + DX(I)*DY(I)
      END DO
   END IF
END IF

RETURN
END FUNCTION DDOT

! --------------------------- DNRM2 -------------------------------------------

REAL(8) FUNCTION DNRM2(N,X,INCX)
!     .. SCALAR ARGUMENTS ..
INTEGER INCX,N
!     ..
!     .. ARRAY ARGUMENTS ..
REAL(8) X(*)
!     ..
!
!  PURPOSE
!  =======
!
!  DNRM2 RETURNS THE EUCLIDEAN NORM OF A VECTOR VIA THE FUNCTION
!  NAME, SO THAT
!
!     DNRM2 := SQRT( X'*X )
!
!  FURTHER DETAILS
!  ===============
!
!  -- THIS VERSION WRITTEN ON 25-OCTOBER-1982.
!     MODIFIED ON 14-OCTOBER-1993 TO INLINE THE CALL TO DLASSQ.
!     SVEN HAMMARLING, NAG LTD.
!
!  =====================================================================
!
!     .. PARAMETERS ..
REAL(8) ONE,ZERO
PARAMETER (ONE=1.0D+0,ZERO=0.0D+0)
!     ..
!     .. LOCAL SCALARS ..
REAL(8) ABSXI,NORM,SCALE,SSQ
INTEGER IX
!     ..
!     .. INTRINSIC FUNCTIONS ..
INTRINSIC ABS,SQRT
!     ..
IF (N.LT.1 .OR. INCX.LT.1) THEN
   NORM = ZERO
ELSE IF (N.EQ.1) THEN
   NORM = ABS(X(1))
ELSE
   SCALE = ZERO
   SSQ = ONE
   !        THE FOLLOWING LOOP IS EQUIVALENT TO THIS CALL TO THE LAPACK
   !        AUXILIARY ROUTINE:
   !        CALL DLASSQ( N, X, INCX, SCALE, SSQ )
   !
   DO IX = 1,1 + (N-1)*INCX,INCX
      IF (X(IX).NE.ZERO) THEN
            ABSXI = ABS(X(IX))
            IF (SCALE.LT.ABSXI) THEN
               SSQ = ONE + SSQ* (SCALE/ABSXI)**2
               SCALE = ABSXI
            ELSE
               SSQ = SSQ + (ABSXI/SCALE)**2
            END IF
      END IF
   END DO
   NORM = SCALE*SQRT(SSQ)
END IF
!
DNRM2 = NORM
RETURN
!
!     END OF DNRM2.
!
END FUNCTION DNRM2
